let numero = 1 // prompt no html

if (numero > 0){
    console.log(`O número digitado é positivo, ${numero}`); // alert no html
} else if (numero == 0 ){
    console.log(`Seu número é nulo!, ${numero}`);
} else{
    console.log(`O número digitado é negativo, ${numero}`) // alert no html
}